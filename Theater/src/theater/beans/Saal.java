package theater.beans;

public class Saal {
	
	private int saalNr;
	private String saalBez;
	private int sanz;
	
	public Saal() {
		
	}

	public int getSaalNr() {
		return saalNr;
	}

	public void setSaalNr(int saalNr) {
		this.saalNr = saalNr;
	}

	public String getSaalBez() {
		return saalBez;
	}

	public void setSaalBez(String saalBez) {
		this.saalBez = saalBez;
	}

	public int getSanz() {
		return sanz;
	}

	public void setSanz(int sanz) {
		this.sanz = sanz;
	}
	
	

}
