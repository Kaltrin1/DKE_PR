package theater.beans;

public class Theaterstueck {

	private String name;
	private String kurzBeschr;
	private String musik;
	private String inszenierung;
	
	public Theaterstueck() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getKurzBeschr() {
		return kurzBeschr;
	}

	public void setKurzBeschr(String kurzBeschr) {
		this.kurzBeschr = kurzBeschr;
	}

	public String getMusik() {
		return musik;
	}

	public void setMusik(String musik) {
		this.musik = musik;
	}

	public String getInszenierung() {
		return inszenierung;
	}

	public void setInszenierung(String inszenierung) {
		this.inszenierung = inszenierung;
	}
	
	
	
}
